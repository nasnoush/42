#include "minitalk.h"

int	ft_atoi(char *str)
{
	int	i;
	int	r;
	int	s;

	i = 0;
	r = 0;
	s = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + str[i] - '0';
		i++;
	}
	return (r * s);
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while(str[i] != '\0')
		i++;
	return (i);
}

void	aff_message(char *str)
{
	int		i;
	char	*message;

	i = 0;
	message = malloc(ft_strlen(str) + 1);
	if (!message)
		return ;
	while (str[i] != '\0')
	{
		message[i] = str[i];
		i++;
	}
	message[i] = '\0';
	ft_printf("%s\n", message);
	free(message);
}
